#!/usr/bin/python3
def add(a, b):
    """Add two numbers and return the result.
    
    Arg:
    a = first number
    b = second number

    Returns:
    the sum of a and b
    """
    return a + b

def sub(a, b):
        """My subtraction function
            
            Args:
                a (int): first number
                b (int): second number
    
            Returns:
                int: the difference of a and b
            """
        return a - b 

def mul(a, b):
        """My subtraction function
            
            Args:
                a (int): first number
                b (int): second number
    
            Returns:
                int: the difference of a and b
            """
        return a * b 

def div(a, b):
        """My subtraction function
            
            Args:
                a (int): first number
                b (int): second number
    
            Returns:
                int: the difference of a and b
            """
        return int(a / b)
    